package study2;

/*
 * 사칙연산
 * */
public class BasicTwo {

    // Type
    // 정수 : int
    // 실수 : double, float
    public static void main(String[] args) {
        // 1 + 1 = 2
        // 선언
        int number = 1;
        int number2 = 1;

        // 출력
        System.out.println(number + number2);
        // 출력
        System.out.println(number - number2);
        // 출력
        System.out.println(number * number2);
        // 출력
        System.out.println(number / number2);
        System.out.println(number % number2);
    }

}