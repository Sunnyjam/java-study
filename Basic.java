/*
* 사칙연산
* */
public class Basic {

     // Type
     // 정수 : int
    // 실수 : double, float
    public static void main(String[]args) {
        // 1 + 1 = 2
        // 선언
        int number = 1;
        int number2 = 1;

        // 출력
        System.out.println(number + number2);

        // 1 - 1 = 0
        // 선언
        int number3 = 1;
        int number4 = 1;

        // 출력
        System.out.println(number3 - number4);

        // 1 x 1 = 1
        // 선언
        int number5 = 1;
        int number6 = 1;

        // 출력
        System.out.println(number5 * number6);
        // 1 / 1 = 1
        // 선언
        int number7 = 1;
        int number8 = 1;

        // 출력
        System.out.println(number7 / number8);
        System.out.println(number7 % number8);
    }

}